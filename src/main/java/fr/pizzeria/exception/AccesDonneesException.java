package fr.pizzeria.exception;

public class AccesDonneesException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AccesDonneesException() {
		super();
	}

	public AccesDonneesException(String message, Throwable cause) {
		super(message, cause);
	}

	public AccesDonneesException(String message) {
		super(message);
	}

	public AccesDonneesException(Throwable cause) {
		super(cause);
	}
	
	

}
